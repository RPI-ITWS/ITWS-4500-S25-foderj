/*Responsible for Curating html for a weather visualization response of all the data */
const { useEffect, useRef, useState } = React;

function getAvgWeath(d){
   if(d.hourly === undefined){
      return(0);
   }
   var sum = 0
   for(var i = 0; i < d.hourly.temperature_2m.length; i++){
      sum += d.hourly.temperature_2m[i]; 
   }
   return(sum/d.hourly.temperature_2m.length); 
}

function getDay(d){
   const date = new Date(d.start_date);
   return(date.getDate());
}


const makeWeathVis = () => {
   const svgRef = useRef(null);
   // const [data, setData] = useState(null);
   
   // Declare the chart dimensions and margins.
   const width = 928;
   const height = 500;
   const marginTop = 30;
   const marginRight = 0;
   const marginBottom = 60;
   const marginLeft = 40;
   //so that elemetn can stay dynamic if needed, second array tells react when to re-run the effect 
   useEffect(() => {



      // Clear previous SVG if re-rendered
      d3.select(svgRef.current).selectAll("*").remove();
      
      const svg = d3.select(svgRef.current)
         .append("svg")
         .attr("width", width)
         .attr("height", height)
         .attr("viewBox", [0, 0, width, height])
         .attr("style", "max-width: 100%; height: auto;");
      
      
      const x = d3.scaleBand()
      //   .domain(d3.groupSort(data, ([d]) => -d.frequency, (d) => d.letter))
         .domain(data.map(getDay))
         .range([marginLeft, width - marginRight])
         .padding(0.1);
      
      const y = d3.scaleLinear()
         .domain([0, d3.max(data, getAvgWeath)])
         .nice()
         .range([height - marginBottom, marginTop]);
      
      svg.append("g")
         .attr("fill", "steelblue")
         .selectAll("rect") // <-- FIXED
         .data(data)
         .join("rect")
         .attr("x", (d) => x(getDay(d)))  // Use x-scale to position the bars based on the day value
         .attr("y", (d) => y(getAvgWeath(d)))
         .attr("height", (d) => y(0) - y(getAvgWeath(d)))
         .attr("width", x.bandwidth());
      
      svg.append("g")
         .attr("transform", `translate(0,${height - marginBottom})`)
         .call(d3.axisBottom(x).tickSizeOuter(0));
      
      svg.append("g")
         .attr("transform", `translate(${marginLeft},0)`)
         .call(d3.axisLeft(y).tickFormat(d3.format(".1f")))

         .call(g => g.select(".domain").remove())
         .call(g => g.append("text")
            .attr("x", -marginLeft)
            .attr("y", 10)
            .attr("fill", "currentColor")
            .attr("text-anchor", "start")
            .text("Temperature (F)"));


      // Add the X-axis label
      svg.append("text")
         .attr("y", 480)        // Position the label at the left side of the chart
         .attr("x", 470)       // Center the label vertically
         .style("text-anchor", "middle")
         .style("font-size", "16px") 
         .text("Day in January");

      //add y axis label
      // Add the Y-axis label
      svg.append("text")
         .attr("transform", "rotate(-90)")  // Rotate the text for vertical alignment
         .attr("y", -50)        // Position the label at the left side of the chart
         .attr("x", 0 - (height / 2))       // Center the label vertically
         .style("text-anchor", "middle")    // Align text to the center
         .style("font-size", "16px")        // Set the font size
         .text("Frequency");     

            
      }, []);
      

   //the element being created will inherit everything tagged to svgRef
   return React.createElement("div", { ref: svgRef, id: "Area" });
};


export default makeWeathVis